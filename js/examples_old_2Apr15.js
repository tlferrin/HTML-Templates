var buildHighlights = function() {
	//Empty the list so it doesn't repeat itself
	document.getElementById("highlights").innerHTML = "";

    for (var i = 0; i < highlights.length; i++) {
    	//A quick shortcut
    	var hl = highlights[i];
		
        var article = document.createElement("article");
        article.className = "item";

        var picture = document.createElement("div");
        picture.className = "picture";
        article.appendChild(picture);

        var img = document.createElement("img");
        img.src = "https://i.ytimg.com/vi_webp/" + hl.URLbit + "/mqdefault.webp";
        picture.appendChild(img);

        var descr = document.createElement("div");
        descr.className = "description";
        article.appendChild(descr);

        var head3 = document.createElement("h3");
        descr.appendChild(head3);

        var descrTitle = document.createTextNode(hl.title);
        head3.appendChild(descrTitle);

        var para1 = document.createElement("p");
        descr.appendChild(para1);

        var descrPerson = document.createTextNode(hl.person);
        para1.appendChild(descrPerson);

        var para2 = document.createElement("p");
        descr.appendChild(para2);

        var descrDept = document.createTextNode(hl.department);
        para2.appendChild(descrDept);

        var para3 = document.createElement("p");
        descr.appendChild(para3);

        var descrDate = document.createTextNode(hl.date);
        para3.appendChild(descrDate);

        document.getElementById("highlights").appendChild(article);
    }
};






