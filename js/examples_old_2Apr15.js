var setBgSelect = function() {
	var items = document.getElementsByClassName("item");
	for (var k=0; k<items.length; k++) {
		items[k].onclick = function () {
			bgSelect(this, "item");
			switchMediaContent(this, "item");
		};
	}
};




//———————————————————————————————————————
//
//		SWITCHES BACKGROUND COLOR FOR
// 		SELECTED ITEM
//
//———————————————————————————————————————

var bgSelect = function(a, thisClass) {

	//Makes the thing the only ".item" or ".mediatype" that is also ".selected"
    var siblings = document.getElementsByClassName(thisClass);
    for (var i = 0; i < siblings.length; i++) {
        siblings[i].classList.remove("selected");
    }
    a.classList.add("selected");
};





//———————————————————————————————————————
//
//	Switches what media shows up when
// 	you click on an option on the left
//
//———————————————————————————————————————

var switchMediaType = function(a, thisClass) {

    //Changes the background color to make it look selected
    bgSelect(a, thisClass);

    //
    //	Deletes all media lists and previewbox contents,
    //	hides the preview box
    //
    var stuffToEmpty = ["highlights", "highlight", "speeches", "speech", "quotes", "quote"];
    for(var i=0; i<stuffToEmpty.length; i++) {
	    var thisThing = document.getElementById(stuffToEmpty[i]);
    	thisThing.innerHTML = "";
	}
	
	var pBox = document.getElementById("previewbox");
	pBox.style.display = "none";

	//Second, empty the preview box
	for(var j=0; j<pBox.childNodes.length; j++) {
    	pBox.childNodes[j].innerHTML = "";
    }

    //Builds the pertinent media list, makes its items selectable.
    if (a.getElementsByTagName("p")[0].innerHTML === "Highlight Videos") {
        buildHighlights();
        setBgSelect();
    } else if (a.getElementsByTagName("p")[0].innerHTML === "Speeches") {
        buildSpeeches();
        setBgSelect();
    } else if (a.getElementsByTagName("p")[0].innerHTML === "Text &amp; Quotes") {
        buildQuotes();
        setBgSelect();
    }
};



//———————————————————————————————————————
//
//	Switches what media is in the previewbox
//  when you click on a list item
//
//———————————————————————————————————————

var switchMediaContent = function(a, thisClass) {

	//Changes the background color to make it look selected
    bgSelect(a, thisClass);

    //Makes the preview box show up
	var pBox = document.getElementById("previewbox");
	pBox.style.display = "block";
	
	//Empties the preview box
    var stuffToEmpty = ["highlight", "speech", "quote"];
    for(var i=0; i<stuffToEmpty.length; i++) {
	    var thisThing = document.getElementById(stuffToEmpty[i]);
    	thisThing.innerHTML = "";
	}

	//
	// NOW, TIME TO BUILD!
	//

	// First, count which child the selected element is.
	var parent = a.parentNode;
	var index = Array.prototype.indexOf.call(parent.children, a);


	//Next, create the appropriate media content
	if(a.parentElement.id === "highlights") {

		//Create the video iframe
		var hbit = highlights[index].URLbit;
		var vid = document.createElement("embed");
		vid.src = "https://www.youtube.com/embed/" + hbit + "?rel=0";
		vid.setAttribute('frameborder', 0);
		vid.setAttribute('allowfullscreen', true);

		document.getElementById("highlight").appendChild(vid);
	}

	else if(a.parentElement.id === "speeches") {

		//Create the video iframe
		var sbit = speeches[index].URLbit;
		var vid = document.createElement("embed");
		vid.src = "https://www.youtube.com/embed/" + sbit + "?rel=0";
		vid.setAttribute('frameborder', 0);
		vid.setAttribute('allowfullscreen', true);

		document.getElementById("speech").appendChild(vid);
	}
	else if(a.parentElement.id === "quotes") {

		var quoteBlock = quotes[index].fullQuote;
		var quotePerson = quotes[index].person;
		var quoteDept = quotes[index].department;

		//Create the quote marks
		var quo1 = document.createElement("h2");
		var quo2 = document.createTextNode('“');
		quo1.appendChild(quo2);
		
		var quo3 = document.createElement("h2");
		var quo4 = document.createTextNode('”');
		quo3.appendChild(quo4);


		//Create the quoteText div and quote contents
		var quoteText = document.createElement("div");
		quoteText.id = "quoteText";

		var para = document.createElement("p");
		var fullText = document.createTextNode(quoteBlock);
		para.appendChild(fullText);

		var quoteAttr = document.createElement("p");
		var quoteAttr1 = document.createTextNode("— " + quotePerson);
		quoteAttr.appendChild(quoteAttr1);

		var quoteAttr2 = document.createElement("span");
		var quoteAttr3 = document.createTextNode(", " + quoteDept);
		quoteAttr2.appendChild(quoteAttr3);
		quoteAttr2.style.fontStyle = "italic";
		quoteAttr2.style.fontWeight = "normal";

		quoteAttr.appendChild(quoteAttr2);

		quoteText.appendChild(para);
		quoteText.appendChild(quoteAttr);


		//Put the quote marks and quoteText div in the quote div
		document.getElementById("quote").appendChild(quo1);
		document.getElementById("quote").appendChild(quoteText);
		document.getElementById("quote").appendChild(quo3);
	}
};










